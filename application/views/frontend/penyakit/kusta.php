<b>Pengertian Kusta</b>
<p>Kusta adalah penyakit yang disebabkan oleh Mycobacterium leprae, yang menyerang kulit dan jaringan saraf perifer serta mata dan selaput yang melapisi bagian dalam hidung.</p>
<p>WHO mengklasifikasikan kusta ke dalam 2 kelompok, yaitu:</p>
<ul>
    <li>Pausibasiler: 1-5 lesi, kusta jenis ini menyebabkan rasa baal yang jelas dan menyerang satu cabang saraf.</li>
    <li>Multibasiler: lesi >5, kusta multibasiler tak seperti pausibasiler, rasa baalnya tidak jelas, dan menyerang banyak cabang saraf.</li>
</ul>

<b>Faktor Risiko Kusta</b>
<p>Terdapat beberapa faktor yang bisa meningkatkan risiko penyakit kusta. Misalnya, memiliki kelainan genetik pada sistem imun, kontak fisik dengan hewan penyebar bakteri kusta seperti armandillo, atau tinggal di area endemik kusta.</p>

<b>Penyebab Kusta</b>
<p>Penyakit Kusta disebabkan oleh bakteri Mycobacterium leprae (M. leprae), sejenis bakteri yang tumbuh dengan lambat. Penularan kusta bisa melalui kontak kulit yang lama dan erat dengan pengidapnya. Di samping itu, kusta juga bisa ditularkan lewat inhalasi alias menghirup udara. Alasannya bakteri penyebab kusta dapat hidup beberapa hari dalam bentuk droplet di udara. Namun, sebenarnya penyakit kusta bukanlah penyakit yang mudah untuk menular.</p>
<p>Hal yang perlu diperhatikan adalah kusta juga bisa menular lewat kontak langsung dengan binatang tertentu, seperti armadillo. Kusta memerlukan waktu inkubasi yang cukup lama antara 40 hari sampai 40 tahun. Rata-rata membutuhkan 3-5 tahun setelah tertular sampai timbulnya gejala.</p>

<b>Gejala Kusta</b>
<p>Gejala utama kusta, yaitu bercak perubahan warna menjadi lebih putih dan lesi di kulit berbentuk benjolan yang tidak hilang setelah beberapa minggu atau lebih. Lesi kuit juga disertai gejala kebas pada bagian tersebut dan kelemahan otot.</p>
<p>Penyakit kusta juga bisa menyebabkan gejala lain pada kulit. Kondisi ini bergantung dari pertumbuhan bakteri itu sendiri, dan jenis-jenis kusta akan mempengaruhi bagaimana kita mengobatinya.</p>
<ol>
    <li>Tuberkuloid</li>
    <p>Jenis kusta yang paling ringan. Orang dengan tipe ini hanya memiliki satu atau beberapa bercak datar berwarna pucat (kusta paucibacillary) disingkat PB. Daerah kulit yang terkena bisa mati rasa karena kerusakan saraf di bawahnya. Kusta tuberkuloid kurang menular dari jenis-jenis lainnya.</p>
    <li>Lepromatosa</li>
    <p>Jenis kusta yang lebih parah. Pengidap kusta jenis ini akan memiliki benjolan luas di kulit dan ruam (kusta multibasiler), mengalami mati rasa, dan kelemahan otot. Selain itu, hidung, ginjal, dan organ reproduksi laki-laki juga dapat terpengaruh. Kusta lepromatosa lebih menular dari kusta tuberkuloid.</p>
    <li>Borderline</li>
    <p>Pada tipe ini, seseorang memiliki gejala gabungan dari kusta jenis tuberkuloid dan jenis lepromatosa.</p>
</ol>

<b>Diagnosis Kusta</b>
<p>Dokter dapat meminta pemeriksaan berikut ini:</p>
<ul>
    <li>Pemeriksaan bakterioskopik dibuat dari kerokan jaringan kulit di beberapa tempat, diperiksa di bawah mikroskop untuk melihat adanya bakteri Lepra.</li>
    <li>Pemeriksaan histopatologis bertujuan untuk melihat perubahan jaringan dikarenakan infeksi.</li>
    <li>Pemeriksaan serologis didasarkan atas terbentuknya antibodi pada tubuh seseorang akibat infeksi.</li>
</ul>
<p>Selain mengidentifikasi tiga tanda utama (lesi kulit yang mati rasa, penebalan saraf tepi, dan hasil bakterioskopik positif) dokter dapat menegakkan diagnosis.</p>

<b>Komplikasi Kusta</b>
<p>Kusta yang tak ditangani dengan cepat dan efektif bisa menimbulkan berbagai komplikasi, yaitu:</p>
<ul>
    <li>Kebutaan atau glaukoma.</li>
    <li>Disfigurasi wajah, termasuk pembengkakan permanen dan benjolan.</li>
    <li>Gagal ginjal.</li>
    <li>Kelemehan otot yang mengarah ke tangan.</li>
    <li>Ketidakmampuan melenturkan kaki.</li>
    <li>Kerusakan permanen pada saraf di luar otak dan sumsum tulang belakang.</li>
    <li>Cacat progresif atau kerusakan permanen pada bigian hidung, alis, atau jari kaki.</li>
    <li>Disfungsi ereksi dan kemandulan pada pria.</li>
</ul>

<b>Pengobatan Kusta</b>
<p>Tujuan utama pengobatan kusta adalah untuk memutuskan mata rantai penularan, menurunkan insiden penyakit, mengobati dan menyembuhkan pengidap, serta mencegah timbulnya kecacatan. Agar pasien bisa sembuh dan mencegah terjadinya resistensi, pengobatan kusta menggunakan kombinasi beberapa antibiotik.</p>
<p>Pengidap kusta diberikan kombinasi antibiotik selama 6 bulan hingga 2 tahun. Jenis antibiotik, dosis, dan durasi pengunaannya ditentukan berdasarkan jenis kusta. Penanganan kusta sebenarnya tidak cuma melalui obat-obatan saja. Penanganan penyakit ini juga bisa melalui tindakan pembedahan. Tujuan prosedur pembedahan bagi pengidap kusta, yaitu:</p>
<ul>
    <li>Menormalkan fungsi saraf yang rusak.</li>
    <li>Memperbaiki bentuk tubuh pengidap yang cacat.</li>
    <li>Mengembalikan fungsi anggota tubuh.</li>
</ul>

<b>Pencegahan Kusta</b>
<p>Terdapat beberapa upaya yang bisa dilakukan untuk mencegah kusta, antara lain:</p>
<ul>
    <li>Edukasi mengenai kusta terutama di daerah endemik.</li>
    <li>Melakukan diagnosis dini dan pengobatan untuk mencegah penularan.</li>
    <li>Sampai saat ini belum ada vaksin.</li>
</ul>

<b>Kapan Harus ke Dokter?</b>
<p>Bila mengalami geja-gejala di atas, segeralah temui dokter untuk mendapatkan penanganan yang tepat. Penanganan yang tepat dapat meminimalisir akibat, sehingga pengobatan bisa lebih cepat dilakukan.</p>