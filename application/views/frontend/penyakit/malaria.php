<b>Pengertian Penyakit Malaria</b>
<p>Malaria disebabkan oleh parasit yang menyebar melalui gigitan nyamuk dan dapat mematikan jika tidak ditangani dengan benar. Malaria jarang sekali menular secara langsung dari satu orang ke orang lainnya.</p>
<p>Penularannya bisa terjadi jika ada kontak dengan darah pengidap atau janin yang bisa terinfeksi karena tertular dari darah ibu.</p>

<b>Gejala Malaria</b>
<p>Penyakit malaria mempunyai trias, yaitu keadaan menggigil yang diikuti dengan demam dan keluar keringat yang banyak. Beberapa gejala lain yang dapat ditemui juga, yaitu :</p>
<ol>
    <li>Demam : bersifat periodik karena berkaitan dengan pecahnya skizon yang mengeluarkan berbagai antigen. Proses pematangan skizon berbeda tiap jenis plasmodium.</li>
    <ul>
        <li>P. falciparum (demam hampir setiap hari)</li>
        <li>P. vivax/ovale (demam setiap 3 hari/tertiana)</li>
        <li>P. malariae (demam setiap 4 hari / kuartana)</li>
    </ul>
    <li>Splenomegali : merupakan gejala malaria kronik.</li>
    <li>Anemia : terjadi akibat pecahnya eritrosit yang terinfeksi maupun tidak.</li>
    <li>Iketerus : karena terjadinya hemolysis dan gangguan hepatik.</li>
    <li>Gejala sistemik lainnya : sakit kepala, mual muntah, nyeri otot.</li>
</ol>

<b>Penyebab dan Faktor Risiko Malaria</b>
<p>Plasmodium terdiri dari banyak spesies, tetapi umumnya menularkan malaria adalah P.vivax, P.falciparum, P.malariae, P.ovale. daur hidup plasmodium terdiri dari fase seksual di dalam badan nyamuk anopheles betina sebagai hospes definitif dan fase aseksual dalam tubuh manusia.</p>

<b>Diagnosis Malaria</b>
<p>Selain berdasarkan anamnesis (trias malaria, disertai sakit kepala, mual muntah, nyeri otot) serta riwayat bepergian dan pemeriksaan fisik (demam, konjungtiva anemis, splenomegali, ikterik, dan sebagainya), dibutuhkan pemeriksaan penunjang untuk menegakkan diagnosis definitif, yaitu:</p>
<ol>
    <li>Pemeriksaan sediaan darah tebal dan tipis untuk menentukan ada tidaknya spesies, stadium dari plasmodium.</li>
    <li>Rapid diagnostic test (RDT).</li>
    <li>Pemeriksaan untuk malaria berat (apabila ditemukan P.falciparum disertai dengan salah satu gejala kegawatan).</li>
</ol>

<b>Penanganan Malaria</b>
<p>Penanganan dilakukan dengan pemberian obat antimalaria, yang terdiri dari 4 jenis :</p>
<ol>
    <li>Skizontisid jaringan primer (untuk parasit praeritrosit)</li>
    <li>Skizontisid jaringan sekunder (untuk parasite eksoeritrosit)</li>
    <li>Skizontisid darah (fase eritrosit)</li>
    <li>Gametosit</li>
</ol>

<b>Pencegahan Penyakit Malaria</b>
<p>Penularan penyakit malaria dapat dicegah dengan meningkatkan kewaspadaan dan kepedulian terhadap risiko gigitan nyamuk dan kebersihan lingkungan. Pencegahan gigitan nyamuk dapat dilakukan dengan menggunakan kelambu berinsektisida, atau kawat kasa nyamuk, dan sebagainya.</p>
<p>Obat yang digunakan untuk kemoprofilaksis adalah doksisiklin 100mg/hari dimunum 2-3 hari sebelum bepegian ke daerah endemik malaria sampai dengan 4 minggu setelah pulang (tidak boleh dikonsumsi lebih dari 6 bulan). Tidak boleh diberikan pada ibu hamil dan anak di bawah 8 tahun.</p>

<b>Kapan Harus ke Dokter?</b>
<p>Jika memiliki beberapa gejala seperti yang disebutkan di atas, dan berlangsung selama beberapa minggu atau menjadi lebih parah, segera kunjungi dokter. Jangan hanya menganggap itu sebagai penyakit biasa, sebab banyak penyakit ini lebih mudah diatasi jika dilakukan pemeriksaan sedini mungkin. Penanganan yang tepat dapat meminimalisir akibat sehingga pengobatan bisa lebih cepat dilakukan. Pilih dokter di rumah sakit yang tepat sesuai dengan kebutuhan kamu di sini.</p>