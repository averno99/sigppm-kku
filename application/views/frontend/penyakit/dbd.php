<b>Pengertian Demam Berdarah</b>
<p>DBD atau demam berdarah dengue adalah penyakit yang disebabkan oleh salah satu dari empat virus dengue. Demam berdarah merupakan penyakit yang mudah menular. Sarana penularan demam berdarah sendiri berasal dari gigitan nyamuk Aedes aegypti dan Aedes albocpictus.</p>

<b>Faktor Risiko Demam Berdarah</b>
<p>Demam berdarah dapat dipicu oleh faktor risiko tertentu. Beberapa faktor risiko demam berdarah, yaitu:</p>
<ul>
    <li>Pernah mengalami infeksi virus dengue sebelumnya;</li>
    <li>Tinggal atau bepergian ke daerah tropis; dan</li>
    <li>Bayi, anak-anak, orang lanjut usia, dan orang dengan kekebalan tubuh yang lemah.</li>
</ul>

<b>Penyebab Demam Berdarah</b>
<p>Penyakit demam berdarah disebabkan oleh virus dengue yang ditularkan oleh nyamuk Aedes aegypti dan Aedes albopictus. Kedua nyamuk dapat menggigit di pagi hari sampai sore menjelang petang. Penularan terjadi saat nyamuk menggigit dan menghisap darah seseorang yang sudah terinfeksi virus dengue, ketika nyamuk tersebut mengigit orang lain, maka virus akan tersebar. Hal tersebut terjadi karena nyamuk berperan sebagai medium pembawa (carrier) virus dengue tersebut</p>

<b>Gejala Demam Berdarah</b>
<p>Gejala umumnya timbul 4-7 hari sejak gigitan nyamuk, dan dapat berlangsung selama 10 hari. Beberapa gejala demam berdarah, yaitu:</p>
<ul>
    <li>Demam tinggi mencapai 40 derajat Celsius;</li>
    <li>Nyeri kepala berat;</li>
    <li>Nyeri pada sendi, otot, dan tulang;</li>
    <li>Nyeri pada bagian belakang mata;</li>
    <li>Nafsu makan menurun;</li>
    <li>Mual dan muntah;</li>
    <li>Pembengkakan kelenjar getah bening;</li>
    <li>Ruam kemerahan sekitar 2-5 hari setelah demam;</li>
    <li>Kerusakan pada pembuluh darah dan getah bening; dan</li>
    <li>Perdarahan dari hidung, gusi, atau di bawah kulit.</li>
</ul>

<b>Diagnosis Demam Berdarah</b>
<p>Diagnosis penyakit demam berdarah akan dilakukan dengan melakukan pemeriksaan fisik dan wawancara medis. Selain itu pemeriksaan penunjang, seperti pemeriksaan darah di laboratorium juga harus dilakukan. Sebaiknya, setelah merasakan ada gejala-gejala demam berdarah, segera konsultasi dokter di Halodoc atau langsung kunjungi rumah sakit terdekat, agar bisa langsung dilakukan diagnosis. </p>

<b>Komplikasi Demam Berdarah</b>
<p>Saat demam berdarah terlambat untuk ditangani, maka komplikasi akan terjadi. Komplikasi demam berdara atau dengue shock syndrome (DSS) memiliki beberapa gejala dan tanda, yaitu:</p>
<ul>
    <li>Tanda perdarahan, seperti mimisan, gusi berdarah, perdarahan di bawah kulit, muntah hitam, batuk darah, maupun buang air besar dengan feses kehitaman;</li>
    <li>Tekanan darah menurun;</li>
    <li>Kulit basah dan terasa dingin;</li>
    <li>Denyut nadi melemah;</li>
    <li>Frekuensi buang air kecil menurun dan jumlah urine yang keluar sedikit;</li>
    <li>Mulut kering; dan</li>
    <li>Sesak nafas atau pola napas tidak beraturan.</li>
</ul>
<p>Penanganan yang tepat dan cepat harus dilakukan ketika pengidap sudah mengalami DSS. Jika tidak segera dilakukan penanganan, maka bisa mengakibatkan gangguan fungsi organ tubuh yang berujung pada kematian.</p>

<b>Pengobatan Demam Berdarah</b>
<p>Pengobatan yang spesifik untuk mengobati demam berdarah saat ini belum ada. Pengobatan bertujuan untuk mengatasi gejala dan mencegah infeksi virus semakin memberat. Beberapa upaya yang dianjurkan dokter, yaitu:</p>
<ul>
    <li>Banyak minum cairan agar terhindar dari dehidrasi;</li>
    <li>Cukup istirahat;</li>
    <li>Konsumsi obat penurun panas yang relatif aman dan dianjurkan dokter;</li>
    <li>Menghindari konsumsi obat-obatan pereda nyeri. Hal ini dikarenakan obat-obatan tersebut dapat menimbulkan komplikasi perdarahan. </li>
    <li>Pantau frekuensi buang air kecil dan jumlah urine yang keluar.</li>
</ul>

<b>Pencegahan Demam Berdarah</b>
<p>Terdapat berbagai upaya yang dapat dilakukan untuk mencegah demam berdarah, yaitu:</p>
<ul>
    <li>Anak usia 9-16 tahun seharusnya divaksinasi dengue, sebanyak 3 kali dengan jarak 6 bulan;</li>
    <li>Memberantas sarang nyamuk yang dilakukan dalam dua kali pengasapan insektisida atau fogging dengan jarak 1 minggu;</li>
    <li>Menguras tempat penampungan air, seperti bak mandi, minimal setiap minggu;</li>
    <li>Menutup rapat tempat penampungan air;</li>
    <li>Melakukan daur ulang barang yang berpotensi menjadi tempat perkembangbiakan nyamuk Aedes aegypti;</li>
    <li>Mengatur cahaya yang cukup di dalam rumah;</li>
    <li>Memasang kawat anti nyamuk di ventilasi rumah;</li>
    <li>Menaburkan bubuk larvasida (abate) pada penampungan air yang sulit dikuras;</li>
    <li>Menggunakan kelambu saat tidur;</li>
    <li>Menanam tumbuhan pengusir nyamuk;</li>
    <li>Menghentikan kebiasaan menggantung pakaian;</li>
    <li>Menghindari wilayah daerah yang rentan terjadi infeksi;</li>
    <li>Mengenakan pakaian yang longgar; dan</li>
    <li>Menggunakan krim anti-nyamuk yang mengandung N-diethylmetatoluamide (DEET), tetapi jangan gunakan DEET pada anak di bawah 2 tahun.</li>
</ul>